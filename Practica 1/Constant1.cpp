#include <iostream>
using namespace std;

// MACROS
#define PI 3.14159    // Assign value to constant
#define NEWLINE '\n'  // Assing value to constant

// MAIN FUNCTION
int main() {

    double r = 5.0;
    double circle;

    circle = 2 * PI * r;        // Circle = 2 * 3.14159 * r;
    cout << circle << NEWLINE;  // cout << circle << '\n';

}